package com.syntax.class6;

public class IncrementAndDecrement {

	public static void main(String[] args) {

		int num=100;
		
		num=num+1;
		num+=1;
		
		System.out.println(num);
		
		num++;//increment of value goes up by 1
		System.out.println(num);
		
		num--;//decrement of value goes down by 1
		
		//10++ CANNOT BE USED WITH numbers, just VARIABLES
		
		float f=10.99f;
		f++;
		
		System.out.println(f);
		
		
		
	}
}
	
	
	
	
	
	

	
	
	
	