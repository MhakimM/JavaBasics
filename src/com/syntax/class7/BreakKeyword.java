package com.syntax.class7;

public class BreakKeyword {

	public static void main(String[] args) {
		
		for (int i=0; i<=4; i++) {
			
			System.out.println("Almost done");
			
			break;
		}
		
		while(true) {
			System.out.println("Hello");
			break;
		}
		
		System.out.println("End of the code");

	}

}
