package com.syntax.class7;

public class ForLoopMultipTable {

	public static void main(String[] args) {

		int num = 1;

		for (int i = 1; i <= 10; i++) {
			System.out.println(num + " x " + i + " = " + num * i);

		}

		System.out.println("===================================");

		for (int i = 1; i <= 20; i += 2) {
			System.out.print(i + " ");

		}
	}
		//System.out.println(i);

	
}

